#include "ps.h"

int	ft_isvalid_char(char c, int position)
{
	if (c >= 0 && c <= 9)
		return (1);
	if ((position == 0) && (c == '+' || c == '-'))
		return (1);
	if ((position == 0) && ((c == ' ') || (c >= 9 && c <= 13)))
		return (1);
	else 
		return(0);
}

